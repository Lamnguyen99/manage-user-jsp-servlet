package com.iviettech.model;

/**
 *
 * @author lamde
 */
public class Role {
    private int roleid;
    private String name;
    private String code;
    
        
    public int getRoleid() {
        return roleid;
    }

    public void setRoleid(int roleid) {
        this.roleid = roleid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
        
}
